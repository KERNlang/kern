#!/usr/bin/env node
/**
 * Idempotent git hook installer. Runs from the `prepare` lifecycle so
 * every `pnpm install` ensures the local clone has the hooks in place.
 *
 * Hooks installed:
 *   pre-push  — runs `pnpm lint` to block obvious format/style errors
 *               from reaching CI. Bypass with `git push --no-verify`
 *               when you genuinely need to.
 *
 * Why a script and not husky/simple-git-hooks: avoids adding another
 * runtime dependency to a repo that already has a strict lockfile gate.
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync, chmodSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const gitDir = join(repoRoot, '.git');

if (!existsSync(gitDir)) {
  // Not a git checkout (e.g. tarball install) — nothing to do.
  process.exit(0);
}

const hooksDir = join(gitDir, 'hooks');
if (!existsSync(hooksDir)) mkdirSync(hooksDir, { recursive: true });

const PRE_PUSH_MARKER = '# kern-managed pre-push hook v1';
const prePushHook = `#!/usr/bin/env bash
${PRE_PUSH_MARKER}
#
# Runs lint before allowing a push. Bypass with \`git push --no-verify\`
# when you genuinely need to (e.g. mid-rebase, work-in-progress branches
# you intend to clean up before merging).
#
# Generated by scripts/install-git-hooks.mjs — do not hand-edit; changes
# are overwritten on next \`pnpm install\`.
set -e

# Skip when nothing is being pushed (e.g. \`git push\` with no refs).
if [ -z "$(git rev-list --max-count=1 HEAD 2>/dev/null)" ]; then
  exit 0
fi

echo "[pre-push] running pnpm lint…"
if ! pnpm lint; then
  echo ""
  echo "[pre-push] pnpm lint failed — push aborted."
  echo "[pre-push] Fix the issues, or run \\\`git push --no-verify\\\` to bypass."
  exit 1
fi
`;

const prePushPath = join(hooksDir, 'pre-push');

// Only overwrite if missing or if it's a previous version of OUR hook.
// Leaves user-customized hooks alone.
let shouldWrite = true;
if (existsSync(prePushPath)) {
  const existing = readFileSync(prePushPath, 'utf-8');
  if (existing === prePushHook) {
    shouldWrite = false;
  } else if (!existing.includes(PRE_PUSH_MARKER)) {
    console.log('[install-git-hooks] .git/hooks/pre-push exists and is not managed by us — leaving it alone.');
    process.exit(0);
  }
}

if (shouldWrite) {
  writeFileSync(prePushPath, prePushHook);
  chmodSync(prePushPath, 0o755);
  console.log('[install-git-hooks] installed .git/hooks/pre-push (runs `pnpm lint`).');
}
